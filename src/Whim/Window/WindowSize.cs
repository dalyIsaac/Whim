namespace Whim;

/// <summary>
/// The size of a window.
/// </summary>
public enum WindowSize
{
	/// <summary>
	/// The window is in its normal state.
	/// </summary>
	Normal,

	/// <summary>
	/// The window is minimized.
	/// </summary>
	Minimized,

	/// <summary>
	/// The window is maximized.
	/// </summary>
	Maximized,
}
